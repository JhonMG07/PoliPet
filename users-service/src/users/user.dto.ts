import { IsString, IsEmail, MinLength, IsOptional, IsEnum } from 'class-validator';

export class CreateUserDto {
  @IsEmail()
  email: string;

  @IsString()
  @MinLength(6)
  password: string;

  @IsString()
  firstName: string;

  @IsString()
  lastName: string;

    // --- AÑADIR ESTAS LÍNEAS ---
  @IsEnum(['admin', 'user', 'shelter'])
  @IsOptional() // Hacemos que sea opcional, si no se envía, la entidad le pondrá 'user' por defecto.
  role?: 'admin' | 'user' | 'shelter';
    // --------------------------

  @IsString()
  @IsOptional()
  phone?: string;

  @IsString()
  @IsOptional()
  address?: string;

  @IsString()
  @IsOptional()
  city?: string;

  @IsString()
  @IsOptional()
  state?: string;

  @IsString()
  @IsOptional()
  zipCode?: string;
}

